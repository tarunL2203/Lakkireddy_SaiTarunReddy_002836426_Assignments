    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblProfiles.getSelectedRow();
        if (selectedRowIndex<0) { 
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
                
        }
        DefaultTableModel model = (DefaultTableModel)   tblProfiles.getModel();
       PersonC selectedProfile = (PersonC) model.getValueAt(selectedRowIndex, 0);
       log.deleteProfile(selectedProfile);
        JOptionPane.showMessageDialog(this, "This profile has been deleted.");
        
        populateProfiles(); 
        
         txtFirstName.setText("");
         txtLastName.setText("");
         txtGender.setText("");
         txtDateofBirth.setText("");
         txtEthnicity.setText("");
         
    }//GEN-LAST:event_btnDeleteActionPerformed

