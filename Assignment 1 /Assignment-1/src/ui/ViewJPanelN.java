    private void populateProfiles() {
        DefaultTableModel model = (DefaultTableModel)   tblProfiles.getModel();
        model.setRowCount(0);
        for (PersonC vs : log.getLog()) {

            Object[] row = new Object[3];
            row[0] = vs;
            row[1] = vs.getLastname();
            row[2] = vs.getDob();

            model.addRow(row);

     }
     
     
         
     }
         
    
