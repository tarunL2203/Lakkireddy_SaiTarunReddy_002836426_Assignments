    private void btnViewLAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewLAActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblLA.getSelectedRow();
        if (selectedRowIndex<0) { 
            JOptionPane.showMessageDialog(this, "Please select a row to view");
            return;
        }
        
         DefaultTableModel model = (DefaultTableModel)   tblLA.getModel();
       LocalAddressC selectedAddress = (LocalAddressC) model.getValueAt(selectedRowIndex, 0);
       
        txtName.setText(selectedAddress.getName());
       txtStreetAddress.setText(selectedAddress.getStreetAddress());
       txtCity.setText(selectedAddress.getCity());
       txtState.setText(selectedAddress.getState());
       txtZIP.setText(selectedAddress.getZip());
       
        
    }//GEN-LAST:event_btnViewLAActionPerformed

    private void btnDeleteLAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteLAActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex = tblLA.getSelectedRow();
        if (selectedRowIndex<0) { 
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        
         DefaultTableModel model = (DefaultTableModel)   tblLA.getModel();
       LocalAddressC selectedAddress = (LocalAddressC) model.getValueAt(selectedRowIndex, 0);
       
         lalog.deleteAddress(selectedAddress);
        JOptionPane.showMessageDialog(this, "This Address has been deleted.");
        
        populateAddresses();
        
          txtName.setText("");
        txtStreetAddress.setText("");
        txtState.setText("");
        txtCity.setText("");
        txtZIP.setText(""); 
        
    }//GEN-LAST:event_btnDeleteLAActionPerformed


