    private void populateAddresses() {
    DefaultTableModel model = (DefaultTableModel) tblLA.getModel();
     model.setRowCount(0);    
     for (LocalAddressC vs : lalog.getLalog()) {
         Object[] row = new Object[3];
         row[0] = vs;
         row[1] = vs.getStreetAddress();
         row[2] = vs.getCity();
         
         model.addRow(row); 
    }
}
